package Lesson2;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 19;
        int b = 50;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);
    }
}
