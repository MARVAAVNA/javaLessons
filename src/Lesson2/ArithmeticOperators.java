package Lesson2;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 62;
        int b = 42;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(--b);
        System.out.println(b--);
    }
}
