package Lesson2;

public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 30;

        a += 12;
        System.out.println(a);

        a -= 3;
        System.out.println(a);

        a *= 5;
        System.out.println(a);

        a /= 3;
        System.out.println(a);

        a %= 9;
        System.out.println(a);

        a <<= 3;
        System.out.println(a);

        a >>= 4;
        System.out.println(a);

        a &= 5;
        System.out.println(a);

        a |= 8;
        System.out.println(a);

        a ^= 2;
        System.out.println(a);
    }
}
